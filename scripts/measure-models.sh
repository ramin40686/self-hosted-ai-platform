load_enabled_models() {

    ENABLED_MODELS=()

    local LINE_NO=0

    while IFS= read -r LINE; do

        ((++LINE_NO))

        [[ -z "$LINE" || "$LINE" =~ ^# ]] && continue

        local TYPE
        local CFG_MODEL
        local STATE
        local PRIORITY
        local TP
        local DEVICE
        local VRAM

        IFS='|' read -r \
            TYPE \
            CFG_MODEL \
            STATE \
            PRIORITY \
            TP \
            DEVICE \
            VRAM <<< "$LINE"

        [[ "$STATE" != "on" ]] && continue

        ENABLED_MODELS+=("${LINE_NO}|${CFG_MODEL}")

    done < "$MODEL_CONFIG"
}

# new function build_temp_config
build_temp_config() {

    local ACTIVE_LINE="$1"
    local ACTIVE_MODEL="$2"

    local LINE_NO=0

    TMP_CONFIG="$(mktemp)"
    TMP_COMPOSE_DIR="$(mktemp -d)"
    local FOUND=0

    TMP_CONFIG="$(mktemp)"
    TMP_COMPOSE_DIR="$(mktemp -d)"

    while IFS= read -r LINE; do
        ((++LINE_NO))

        # comments / empty lines
        if [[ -z "$LINE" || "$LINE" =~ ^# ]]; then
            echo "$LINE" >> "$TMP_CONFIG"
            continue
        fi

        local TYPE
        local CFG_MODEL
        local STATE
        local PRIORITY
        local TP
        local DEVICE
        local VRAM

        IFS='|' read -r \
            TYPE \
            CFG_MODEL \
            STATE \
            PRIORITY \
            TP \
            DEVICE \
            VRAM <<< "$LINE"

        if (( LINE_NO == ACTIVE_LINE )); then
            STATE="on"
        else
            STATE="off"
        fi

        printf "%s|%s|%s|%s|%s|%s|%s\n" \
            "$TYPE" \
            "$CFG_MODEL" \
            "$STATE" \
            "$PRIORITY" \
            "$TP" \
            "$DEVICE" \
            "$VRAM" \
            >> "$TMP_CONFIG"

    done < "$MODEL_CONFIG"

    echo
    echo "=========================================="
    echo "Temporary config for: $ACTIVE_MODEL"
    echo "=========================================="
    cat "$TMP_CONFIG"
}


